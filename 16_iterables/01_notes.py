# Iterables = An object/collection that can return its elements one at a time, 
#             allowing it to be iterated over in a loop

# Iterables = anything you can loop through one element at a time
# Lists, tuples, sets, strings, dictionaries are all iterables
# When you use "for x in something" - that "something" must be an iterable

'''
# Tuple
numbers = (1, 2, 3, 4, 5)

for number in numbers:
    print(number)


# Set
fruits = {"apple", "banana", "orange", "coconut"}

for fruit in fruits:
    print(fruit)


# String
name = "Bro Code"

for character in name:
    print(character, end=" ")



# Dictionary
my_dictionary = {"A": 1, "B": 2, "C": 3}

for key, value in my_dictionary.items():
    print(f"{key} = {value}")
'''