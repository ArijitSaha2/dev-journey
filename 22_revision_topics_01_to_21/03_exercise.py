# Revision - User Input & Arithmetic
# Ask the user for 2 numbers
# Print their sum, difference, product, and remainder

userin1 = int(input("Enter first number : "))
userin2 = int(input("Enter second number: "))

print(f"Sum of the numbers = {userin1 + userin2}")
print(f"Difference of the numbers = {userin1 - userin2}")
print(f"Product of the numbers = {userin1 * userin2}")
print(f"Remainder of the numbers = {userin1 % userin2}")